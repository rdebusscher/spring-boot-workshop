package be.atbash.demo.spring.rest.model;

import jakarta.persistence.*;

import java.io.Serializable;
import java.time.LocalDate;

@Entity
@Table(name = "employee")  // Not strictly needed, but clear because database should not be generated from the code
public class Employee implements Serializable {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private Long id;

    @Column(name = "email", unique = true, nullable = false)
    private String email;

    @Column(name = "first_name", nullable = false)
    private String firstName;

    @Column(name = "last_name", nullable = false)
    private String lastName;

    @Column(name = "hire_date")
    @Temporal(TemporalType.DATE)  // Indicates 'what' is stored in the database (date and/or time information)
    private LocalDate hireDate;

    @Column(name = "gender")
    @Enumerated(EnumType.STRING)  // Should never ordinal as that would mean that changing the order of enum values changes the data.
    private Gender gender;

    @ManyToOne
    @JoinColumn(name = "company_id")
    private Company company;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public LocalDate getHireDate() {
        return hireDate;
    }

    public void setHireDate(LocalDate hireDate) {
        this.hireDate = hireDate;
    }

    public Gender getGender() {
        return gender;
    }

    public void setGender(Gender gender) {
        this.gender = gender;
    }

    public Company getCompany() {
        return company;
    }

    public void setCompany(Company company) {
        this.company = company;
    }

    // equals and hashCode should be on the natural key, email in this case.
    @Override
    public final boolean equals(Object o) {
        if (!(o instanceof Employee employee)) {
            return false;
        }

        return email.equals(employee.email);
    }

    @Override
    public int hashCode() {
        return email.hashCode();
    }
}
